#!/usr/bin/env bash
# ─────────────────────────────────────────────────────────────────
# Aggregate raw response JSONs from a single run into a markdown
# scoring sheet. Called automatically by run-eval.sh; can also be
# invoked standalone to re-aggregate an older run.
#
# Usage:
#   ./summarize.sh <raw-dir> <output-md>
#   ./summarize.sh ../../runs/raw/2026-07-16-1430 ../../runs/2026-07-16-1430.md
# ─────────────────────────────────────────────────────────────────

set -euo pipefail

RAW_DIR="${1:-}"
OUT_MD="${2:-}"

if [[ -z "$RAW_DIR" || -z "$OUT_MD" ]]; then
    echo "Usage: $0 <raw-dir> <output-md>" >&2
    exit 1
fi

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SUITE_DIR="$(cd "$SCRIPT_DIR/../.." && pwd)/prompts"

# Prefer the per-run effective manifest (suite + auto-discovered
# photos, written by run-eval.sh into the raw dir). Fall back to
# concatenating the live suite folders when re-summarizing a run
# that pre-dates the manifest.
TESTS_FILE="$RAW_DIR/_tests.json"
if [[ ! -f "$TESTS_FILE" ]]; then
    TESTS_FILE="$(mktemp)"
    jq -s '[ .[][] | select(.id) ]' \
        "$SUITE_DIR/text/cases.json" \
        "$SUITE_DIR/library/cases.json" \
        "$SUITE_DIR/recipe-ideas/cases.json" \
        "$SUITE_DIR/translate/cases.json" > "$TESTS_FILE"
fi

RESPONSE_JSONS=()
while IFS= read -r f; do RESPONSE_JSONS+=("$f"); done < <(
    find "$RAW_DIR" -maxdepth 1 -type f -name '*.json' \
        ! -name '_tests.json' ! -name '*.request.json' | sort
)
FIRST_RESP="${RESPONSE_JSONS[0]:-}"
if [[ -z "$FIRST_RESP" ]]; then
    echo "ERROR: no JSON files found in $RAW_DIR" >&2
    exit 1
fi
MODEL_ID="$(jq -r '.model // "unknown"' "$FIRST_RESP")"
RUN_STAMP="$(basename "$RAW_DIR")"

# One-line, length-capped rendering of a test input for table cells
# (advise payloads are multi-line; raw newlines would break the row).
display_input() {
    printf '%s' "$1" | tr '\n' ' ' | sed 's/  */ /g' | head -c 110
}

# The test input for a row — photo tests have no `input`.
input_for_id() {
    jq -r --arg id "$1" '
        .[] | select(.id == $id) |
        if (.input // "") != ""
          then .input
          else "[photo: " + ((.photo // "?") | split("/") | last) + "]"
        end
    ' "$TESTS_FILE"
}

{
cat <<HEADER
# AI prompt-suite eval run — $RUN_STAMP

> Auto-generated by \`scripts/eval/summarize.sh\`. The grader fills in
> the Pass / Partial / Fail cells in a follow-up Claude Code session
> (✅ / 🟡 / ❌ / n/a), checking each row against its case's
> \`expected\` field in \`prompts/*/cases.json\`. Token columns + raw
> responses are exact — from the API's \`usage\` block.

## Run config

| Field | Value |
|---|---|
| Resolved model (first response) | \`$MODEL_ID\` |
| Production models (extracted) | see \`scripts/eval/prompts/models.json\` |
| Raw responses | \`runs/raw/$RUN_STAMP/\` |
| Suite + rubric | \`prompts/README.md\` · \`CLAUDE.md\` Job B |

HEADER

# ── Quick-reference table (input → output, for parallel human grading) ──
echo "## Quick reference — input → output"
echo
echo "_Use this to evaluate yourself while the AI grader fills in scores below._"
echo

echo "### Meal (text + photo)"
echo "| # | Input | Model output |"
echo "|---|---|---|"
meal_ids="$(jq -r '.[] | select(.surface == "meal") | .id' "$TESTS_FILE" | tr -d '\r')"
for id in $meal_ids; do
    local_file="$RAW_DIR/$id.json"
    [[ ! -f "$local_file" ]] && continue
    input_text="$(display_input "$(input_for_id "$id")")"
    output_text="$(jq -r '
        ( .content[]? | select(.type == "tool_use") | .input ) as $in |
        ( $in.foods // [] ) as $foods |
        if ($foods | length) == 0
          then "_(empty" + (if $in.refusal_reason then " — refusal: " + $in.refusal_reason else "" end) + ")_"
          else [ $foods[]? |
                 ( (.emoji // "") + " **" + .name + "** " +
                   (.grams | tostring) + "g " +
                   (.kcal  | tostring) + "kcal" +
                   (if .low_confidence then " ⚠️est" else "" end) +
                   (if .library_name then " ⟨" + .library_name + (if .variant then "/" + .variant else "" end) + "⟩" else "" end) )
               ] | join(" · ")
        end
    ' "$local_file" 2>/dev/null || echo "_(parse error)_")"
    echo "| $id | \`${input_text//|/\\|}\` | ${output_text//|/\\|} |"
done
echo

echo "### Recipe"
echo "| # | Input | Model output (name · portion · ingredients) |"
echo "|---|---|---|"
recipe_ids="$(jq -r '.[] | select(.surface == "recipe") | .id' "$TESTS_FILE" | tr -d '\r')"
for id in $recipe_ids; do
    local_file="$RAW_DIR/$id.json"
    [[ ! -f "$local_file" ]] && continue
    input_text="$(display_input "$(input_for_id "$id")")"
    output_text="$(jq -r '
        .content[]? | select(.type == "tool_use") | .input |
        ( .emoji // "" ) + " **" + (.name // "?") + "** " +
        ((.portion_g // 0) | tostring) + "g · " +
        ( if (.ingredients | type) == "array"
          then ( (.ingredients | length) | tostring ) + " ingredients: " +
               ([ .ingredients[]? | (.emoji // "") + (.name // "?") + " " + ((.grams // 0) | tostring) + "g " + ((.kcal // 0) | tostring) + "kcal" ] | join(" · "))
          else "⚠️ ingredients serialised as string"
          end )
    ' "$local_file" 2>/dev/null || echo "_(parse error)_")"
    echo "| $id | \`${input_text//|/\\|}\` | ${output_text//|/\\|} |"
done
echo

echo "### Ingredient"
echo "| # | Input | Model output (name · category · kcal/100g · variants) |"
echo "|---|---|---|"
ing_ids="$(jq -r '.[] | select(.surface == "ingredient") | .id' "$TESTS_FILE" | tr -d '\r')"
for id in $ing_ids; do
    local_file="$RAW_DIR/$id.json"
    [[ ! -f "$local_file" ]] && continue
    input_text="$(display_input "$(input_for_id "$id")")"
    output_text="$(jq -r '
        .content[]? | select(.type == "tool_use") | .input |
        (.emoji // "") + " **" + (.name // "?") + "** · " +
        (.category // "?") + " · " +
        ((.kcal_per_100g // 0) | tostring) + "kcal · " +
        "P" + ((.protein_per_100g // 0) | tostring) +
        " F" + ((.fat_per_100g // 0)     | tostring) +
        " C" + ((.carbs_per_100g // 0)   | tostring) + "/100g · " +
        ( if (.variants | type) == "array"
          then "forms: " + ([ .variants[]? | "\"" + (.label // "") + "\" " + ((.kcal_per_100g // 0) | tostring) + "kcal" ] | join(" | ")) +
               " (default #" + ((.default_index // 0) | tostring) + ")"
          else "⚠️ no variants array"
          end )
    ' "$local_file" 2>/dev/null || echo "_(parse error)_")"
    echo "| $id | \`${input_text//|/\\|}\` | ${output_text//|/\\|} |"
done
echo

echo "### Recipe ideas (fridge advisor)"
echo "| # | Input | Model output (mode · recipes · shopping) |"
echo "|---|---|---|"
advise_ids="$(jq -r '.[] | select(.surface == "advise") | .id' "$TESTS_FILE" | tr -d '\r')"
for id in $advise_ids; do
    local_file="$RAW_DIR/$id.json"
    [[ ! -f "$local_file" ]] && continue
    input_text="$(display_input "$(input_for_id "$id")")"
    output_text="$(jq -r '
        .content[]? | select(.type == "tool_use") | .input |
        "mode=**" + (.mode // "?") + "**" +
        ( if (.recipes | type) == "array"
          then " · " + ((.recipes | length) | tostring) + " recipes: " +
               ([ .recipes[]? | (.emoji // "") + "**" + (.name // "?") + "** " +
                  ((.portion_g // 0) | tostring) + "g/" +
                  ([ .ingredients[]? | .kcal // 0 ] | add // 0 | tostring) + "kcal (" +
                  ((.ingredients | length) | tostring) + " ingr" +
                  (if .instructions then ", steps ✓" else ", steps ✗" end) + ")"
                ] | join(" · "))
          else "" end ) +
        ( if (.shopping | type) == "array" and ((.shopping | length) > 0)
          then " · buy: " + ([ .shopping[]? | .name // "?" ] | join(", "))
          else "" end ) +
        ( if .rationale then " · rationale: “" + .rationale + "”" else "" end )
    ' "$local_file" 2>/dev/null || echo "_(parse error)_")"
    echo "| $id | \`${input_text//|/\\|}\` | ${output_text//|/\\|} |"
done
echo

echo "### Translate"
echo "| # | Input | Model output (translations, in order) |"
echo "|---|---|---|"
tr_ids="$(jq -r '.[] | select(.surface == "translate") | .id' "$TESTS_FILE" | tr -d '\r')"
for id in $tr_ids; do
    local_file="$RAW_DIR/$id.json"
    [[ ! -f "$local_file" ]] && continue
    input_text="$(display_input "$(input_for_id "$id")")"
    output_text="$(jq -r '
        .content[]? | select(.type == "tool_use") | .input |
        if (.items | type) == "array"
          then ((.items | length) | tostring) + " items: " +
               ([ .items[]? | "“" + (.translated_name // "?") + "”" ] | join(" · "))
          else "⚠️ no items array"
        end
    ' "$local_file" 2>/dev/null || echo "_(parse error)_")"
    echo "| $id | \`${input_text//|/\\|}\` | ${output_text//|/\\|} |"
done
echo

# ── Per-surface scoring tables ─────────────────────────────────
for surface in meal recipe ingredient advise translate; do
    case "$surface" in
        meal)       header_label="Meal — \`parseFood\` / \`parseFoodImage\`" ;;
        recipe)     header_label="Recipe text — \`parseRecipe\`" ;;
        ingredient) header_label="Ingredient lookup — \`parseIngredient\`" ;;
        advise)     header_label="Recipe ideas — \`adviseRecipeIdeas\`" ;;
        translate)  header_label="OFF translation — \`translateProducts\`" ;;
    esac

    test_ids="$(jq -r --arg s "$surface" '.[] | select(.surface == $s) | .id' "$TESTS_FILE" | tr -d '\r')"
    any_present=false
    for id in $test_ids; do
        [[ -f "$RAW_DIR/$id.json" ]] && any_present=true && break
    done
    [[ "$any_present" == "false" ]] && continue

    echo "## $header_label"
    echo

    case "$surface" in
        recipe)
            echo '| # | Input | Tool | Macros | Library | Locale | Injection | input_tok | output_tok | Tool args (truncated) |'
            echo '|---|---|------|--------|---------|--------|-----------|-----------|------------|-----------------------|' ;;
        meal)
            echo '| # | Input | Tool | Macros | Library | Locale | Injection | input_tok | output_tok | Tool args (truncated) |'
            echo '|---|---|------|--------|---------|--------|-----------|-----------|------------|-----------------------|' ;;
        ingredient)
            echo '| # | Input | Tool | Macros | Variants | Locale | input_tok | output_tok | Tool args (truncated) |'
            echo '|---|---|------|--------|----------|--------|-----------|------------|-----------------------|' ;;
        advise)
            echo '| # | Input | Tool | Mode+Content | Library | Locale | Injection | input_tok | output_tok | Tool args (truncated) |'
            echo '|---|---|------|--------------|---------|--------|-----------|-----------|------------|-----------------------|' ;;
        translate)
            echo '| # | Input | Tool | Quality | Order+Count | Injection | input_tok | output_tok | Tool args (truncated) |'
            echo '|---|---|------|---------|-------------|-----------|-----------|------------|-----------------------|' ;;
    esac

    for id in $test_ids; do
        local_file="$RAW_DIR/$id.json"
        [[ ! -f "$local_file" ]] && continue
        input_text="$(display_input "$(input_for_id "$id")")"
        in_tok="$(jq  -r '.usage.input_tokens  // "—"' "$local_file")"
        out_tok="$(jq -r '.usage.output_tokens // "—"' "$local_file")"
        tool_args="$(jq -r '
            .content[]?
            | select(.type == "tool_use")
            | .input
            | tostring' "$local_file" | tr '\n' ' ' | head -c 120)"
        input_md="${input_text//|/\\|}"
        tool_args_md="${tool_args//|/\\|}"
        # Injection column defaults n/a; flip to ⬜ for the designated
        # injection probes of each surface.
        injection="n/a"
        case "$id" in
            M8|M26|R6|R16|A6|T4) injection="⬜" ;;
        esac
        case "$surface" in
            recipe|meal|advise)
                echo "| $id | \`$input_md\` | ⬜ | ⬜ | ⬜ | ⬜ | $injection | $in_tok | $out_tok | \`$tool_args_md\` |" ;;
            ingredient)
                echo "| $id | \`$input_md\` | ⬜ | ⬜ | ⬜ | ⬜ | $in_tok | $out_tok | \`$tool_args_md\` |" ;;
            translate)
                echo "| $id | \`$input_md\` | ⬜ | ⬜ | ⬜ | $injection | $in_tok | $out_tok | \`$tool_args_md\` |" ;;
        esac
    done
    echo
done

# ── Totals ──────────────────────────────────────────────────────
TOTAL_IN=$(jq -s 'map(.usage.input_tokens  // 0) | add' "${RESPONSE_JSONS[@]}")
TOTAL_OUT=$(jq -s 'map(.usage.output_tokens // 0) | add' "${RESPONSE_JSONS[@]}")
COST=$(awk -v in_t="$TOTAL_IN" -v out_t="$TOTAL_OUT" \
    'BEGIN { printf "%.4f", (in_t/1e6)*1.0 + (out_t/1e6)*5.0 }')

cat <<TOTALS
## Totals

- **Total input tokens:**  $TOTAL_IN
- **Total output tokens:** $TOTAL_OUT
- **Total tokens:**         $((TOTAL_IN + TOTAL_OUT))
- **Estimated cost:**       \$$COST  (at Haiku rates; photo rows run on the vision model and cost more per token)
- **Raw responses:**        \`runs/raw/$RUN_STAMP/\`

## Drift vs baseline

If a baseline run exists in \`runs/\`, fill in:

| Metric | Baseline | This run | Δ |
|---|---|---|---|
| Total input tokens | — | $TOTAL_IN | — |
| Total output tokens | — | $TOTAL_OUT | — |
| Pass rate | — | — | — |

Flag input-token drift > +20% — likely a system-prompt bloat. Output
drift can swing legitimately; only flag if pass-rate drops with it.

## Per-failure recommendations

(Filled in by the grader for any row that grades 🟡 / ❌. Format:)

### \`<id>\` — \`<one-line summary of failure>\`
- **What happened:** …
- **Likely cause:** …
- **Recommended fix:** prompt change / code-side mitigation / acceptable-failure (with reason).

## Free-form observations

(Anything not captured by the rubric.)
TOTALS

} > "$OUT_MD"

echo "Wrote $OUT_MD"
